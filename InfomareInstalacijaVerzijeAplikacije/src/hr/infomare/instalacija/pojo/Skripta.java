package hr.infomare.instalacija.pojo;


public class Skripta {
    private Integer rbr;
    private String naziv;
    private String txt;

    public Skripta() {
        super();
    }


    public void setRbr(Integer rbr) {
        this.rbr = rbr;
    }

    public Integer getRbr() {
        return rbr;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public String getTxt() {
        return txt;
    }

 
}